public class OperatorsRel {

    public static void main(String[] args) {

        int valor_1 = 1;
        int valor_2 = 2;


        // Utilizando da seguinte forma, vai ser retornado um booleano
        System.out.println("valor_1 == valor_2: " + (valor_1 == valor_2));
        System.out.println("valor_1 != valor_2: " + (valor_1 != valor_2));
        System.out.println("valor_1 > valor_2: " + (valor_1 > valor_2));
        System.out.println("valor_1 < valor_2: " + (valor_1 < valor_2));

    }
}